    public void showPesanan(){
        DefaultTableModel dataModel =(DefaultTableModel) tabelPesanan.getModel();
        int lengthRow = dataModel.getRowCount();
        for (int n=0; n<lengthRow; n++) {
            dataModel.removeRow(0); 
        }
        try{
            Connection conn = (Connection) Server.serverData();
            Statement st = conn.createStatement();
            String sql = "SELECT id_pembeli, nama_pembeli, nama_menu, harga, total, hargatotal from pesanan ORDER BY id_pesanan ASC";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                dataModel.addRow(new Object [] {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6)
                });
            }
            rs.close();
            st.close();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }
    
    public void editData(){
        try{
            Connection conn = (Connection) Server.serverData();
            Statement st = conn.createStatement();
            int updateHarga = Integer.parseInt(tfJumlah.getText()) * Integer.parseInt(tfHarga.getText());
            String sql = "UPDATE pesanan SET total='"+tfJumlah.getText()+"', hargatotal='"+updateHarga+"'"
                    + "WHERE id_pembeli='"+tfIdPelanggan.getText()+"'";
            st.executeUpdate(sql);
            showPesanan();
            JOptionPane.showMessageDialog(null, "EDIT DATA BERHASIL ");
            st.close();
        }catch ( Exception e){
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }
    
    public void hapusData(){
        try{
            Connection conn = (Connection) Server.serverData();
            Statement st = conn.createStatement();
            String sql = "DELETE FROM pesanan where id_pembeli='"+tfIdPelanggan.getText()+"'";
            st.executeUpdate(sql);
            showPesanan();
            JOptionPane.showMessageDialog(null, "DATA BERHASIL DIHAPUS");
            st.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }
    
    public void cariPesanan(){
        DefaultTableModel dataModel =(DefaultTableModel) tabelPesanan.getModel();
        int lengthRow = dataModel.getRowCount();
        for (int n=0; n<lengthRow; n++) {
            dataModel.removeRow(0); 
        }
        try{
            Connection conn = (Connection) Server.serverData();
            Statement st = conn.createStatement();
            String sql = "SELECT id_pembeli, nama_pembeli, nama_menu, harga, total, hargatotal from pesanan where nama_pembeli like '%"+jTextField1.getText()+"%'";
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                dataModel.addRow(new Object [] {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6)        
                });
            }
            rs.close();
            st.close();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error : " + e);
        }
    }
    
    
