    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        
        if (tfIdPelanggan.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Harap Pilih Data Yang Akan Diubah !");
        }else{
            int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Akan Mengubah Data Ini ?", "KONFIRMASI", YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION)  {
                editData();
                clearField();
            }else{
                clearField();
                JOptionPane.showMessageDialog(null, "Data Tidak Jadi Diubah");
            }
        }
        
        
    }//GEN-LAST:event_btnEditActionPerformed

